     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $events = Event::orderBy('created_at', 'desc')->get();
        $users = User::all();
        return view('event.index', [
            'events' => $events,
            'users' => $users
